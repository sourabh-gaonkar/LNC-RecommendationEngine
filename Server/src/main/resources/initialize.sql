CREATE DATABASE test;

use test;

-- USER DETAILS
CREATE TABLE user_details (
	employee_id VARCHAR(6) PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    role ENUM('ADMIN', 'CHEF', 'EMPLOYEE') NOT NULL,
    email VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(64) NOT NULL
);

-- MENU
CREATE TABLE menu (
	item_id INT PRIMARY KEY AUTO_INCREMENT,
    item_name VARCHAR(25) NOT NULL UNIQUE,
    price DECIMAL(10, 2) NOT NULL,
    availability BOOLEAN NOT NULL
);

-- FEEDBACK
CREATE TABLE feedback (
	feedback_id INT PRIMARY KEY AUTO_INCREMENT,
    employee_id VARCHAR(6) NOT NULL,
    item_id INT NOT NULL,
    rating INT NOT NULL CHECK (rating BETWEEN 1 AND 5),
    comment TEXT,
    feedback_date DATE,
    FOREIGN KEY (employee_id) REFERENCES user_details(employee_id),
    FOREIGN KEY (item_id) REFERENCES menu(item_id)
);

CREATE TABLE item_rating (
	item_id INT NOT NULL UNIQUE,
    weekly_rating DECIMAL(5, 2) NOT NULL CHECK (weekly_rating BETWEEN 0 and 5),
    weekly_reviews INT NOT NULL,
    overall_rating DECIMAL(5, 2) NOT NULL CHECK (overall_rating BETWEEN 0 and 5),
    overall_reviews INT NOT NULL,
    FOREIGN KEY (item_id) REFERENCES menu(item_id)
);

-- RECOMMENDATION
CREATE TABLE menu_rollout (
	item_id INT NOT NULL,
    date DATE NOT NULL,
    votes INT NOT NULL,
    PRIMARY KEY (item_id, date),
    FOREIGN KEY (item_id) REFERENCES menu(item_id)
);

-- NOTIFICATION
CREATE TABLE notification (
	notification_id INT PRIMARY KEY AUTO_INCREMENT,
    employee_id VARCHAR(6) NOT NULL,
    message TEXT,
    status ENUM('PENDING', 'DELIVERED', 'READ') DEFAULT 'PENDING',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);


-- TRIGGERS
-- INITIALIZE ITEM RATING
DELIMITER //

CREATE TRIGGER initialize_item_rating AFTER INSERT ON menu
FOR EACH ROW
BEGIN
  INSERT INTO item_rating (item_id, weekly_rating, weekly_reviews, overall_rating, overall_reviews)
  VALUES (NEW.item_id, 0, 0, 0, 0);
END;
//

DELIMITER ;

-- UPDATE ITEM RATING
DELIMITER //

CREATE TRIGGER update_item_rating
AFTER INSERT ON feedback
FOR EACH ROW
BEGIN
    DECLARE total_weekly_reviews INT;
    DECLARE total_overall_reviews INT;
    DECLARE sum_weekly_ratings DECIMAL(10, 2);
    DECLARE sum_overall_ratings DECIMAL(10, 2);
    DECLARE start_of_week DATE;
    
    -- Determine the start of the current week (assuming week starts on Monday)
    SET start_of_week = DATE_SUB(CURDATE(), INTERVAL WEEKDAY(CURDATE()) DAY);

    -- Calculate the total number of weekly reviews for the item
    SELECT COUNT(*), COALESCE(SUM(rating), 0)
    INTO total_weekly_reviews, sum_weekly_ratings
    FROM feedback
    WHERE item_id = NEW.item_id
      AND feedback_date >= start_of_week;

    -- Calculate the total number of overall reviews for the item
    SELECT COUNT(*), COALESCE(SUM(rating), 0)
    INTO total_overall_reviews, sum_overall_ratings
    FROM feedback
    WHERE item_id = NEW.item_id;

    -- Update the item_rating table
    INSERT INTO item_rating (item_id, weekly_rating, weekly_reviews, overall_rating, overall_reviews)
    VALUES (
        NEW.item_id,
        IF(total_weekly_reviews = 0, 0, sum_weekly_ratings / total_weekly_reviews),
        total_weekly_reviews,
        IF(total_overall_reviews = 0, 0, sum_overall_ratings / total_overall_reviews),
        total_overall_reviews
    )
    ON DUPLICATE KEY UPDATE
        weekly_rating = IF(total_weekly_reviews = 0, 0, sum_weekly_ratings / total_weekly_reviews),
        weekly_reviews = total_weekly_reviews,
        overall_rating = IF(total_overall_reviews = 0, 0, sum_overall_ratings / total_overall_reviews),
        overall_reviews = total_overall_reviews;
END //

DELIMITER ;


-- STORED PROCEDURE
-- DELETE MENU ITEM AND ITS REFERENCES
DELIMITER //

CREATE PROCEDURE delete_menu_item(IN itemName VARCHAR(25))
BEGIN
    DECLARE itemId INT;

    -- Get the item_id for the given item_name
    SELECT item_id INTO itemId
    FROM menu
    WHERE item_name = itemName;

    -- If the item exists, delete related entries
    IF itemId IS NOT NULL THEN
        -- Delete from feedback table
        DELETE FROM feedback WHERE item_id = itemId;

        -- Delete from item_rating table
        DELETE FROM item_rating WHERE item_id = itemId;

        -- Delete from menu table
        DELETE FROM menu WHERE item_id = itemId;
    END IF;
END //

